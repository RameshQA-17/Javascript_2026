let day=3

switch (day) 
{
    case 0:
    console.log("sunday");
    //break;

    case 1:
    console.log("monday");
    break;

    case 2:
    console.log("Tuesday");
    break;

    case 3:
    console.log("wednesday");
    //break;
    
    case 4:
    console.log("Thurday");
    //break;

    case 5:
    console.log("friday");
    break;

    case 6:
    console.log("saturday");

    default:
        console.log("invalid range")
        break;
}